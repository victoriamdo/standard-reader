/**
 * The leaf recolored for a solid green field. Leaflet's stock mark is a dark
 * silhouette (#639431) around a light face, and that silhouette muddies to
 * near-invisible against their own #57822B accent — so on solid the silhouette
 * knocks out to white and the face keeps the brand's light green.
 */
const LEAFLET_ON_SOLID_FILLS = ["#FFFFFF", "#D9EA72"] as const;

/**
 * A platform's logo mark at a fixed pixel size. All three are square on a
 * 64×64 viewBox, so they share optical weight at any size without per-platform
 * nudging.
 *
 * `tone: "onSolid"` is for marks sitting on their own brand's saturated fill,
 * where the stock colors would lose contrast against the background.
 */
export function PlatformMark({
  platform,
  size = 18,
  tone = "brand",
}: {
  platform: PublishingPlatform;
  size?: number;
  tone?: "brand" | "onSolid";
}) {
  const common = {
    width: size,
    height: size,
    viewBox: "0 0 64 64",
    fill: "none",
    xmlns: "http://www.w3.org/2000/svg",
    "aria-hidden": true,
    focusable: false,
  } as const;

  if (platform === "leaflet") {
    return (
      <svg {...common}>
        {LEAFLET_PATHS.map((p, i) => (
          <path
            key={p.fill}
            d={p.d}
            fill={tone === "onSolid" ? LEAFLET_ON_SOLID_FILLS[i] : p.fill}
            fillRule="evenodd"
            clipRule="evenodd"
          />
        ))}
      </svg>
    );
  }

  if (platform === "pckt") {
    return (
      <svg {...common}>
        {PCKT_PATHS.map((p) => (
          <path key={p.fill} d={p.d} fill={p.fill} />
        ))}
      </svg>
    );
  }

  return (
    <svg {...common}>
      <path d={OFFPRINT_PATH} fill="currentColor" />
    </svg>
  );
}
